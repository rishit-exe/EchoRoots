// Utility functions
export const getLanguageById = (id) => {
  return languagesData.find(lang => lang.id === parseInt(id));
};

export const getLanguagesByRegion = (region) => {
  return languagesData.filter(lang => 
    lang.region.toLowerCase().includes(region.toLowerCase())
  );
};

export const getEndangeredLanguages = () => {
  return languagesData.filter(lang => 
    lang.status === "Critically Endangered" || lang.status === "Severely Endangered"
  );
};

export const getTotalSpeakers = () => {
  return languagesData.reduce((total, lang) => total + lang.speakers, 0);
};

export const getTotalMediaFiles = () => {
  return languagesData.reduce((total, lang) => total + lang.mediaCount, 0);
};